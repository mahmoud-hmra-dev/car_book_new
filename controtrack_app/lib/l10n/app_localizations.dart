  String tr(String key) {
    final lang = locale.languageCode;
    return _translations[lang]?[key] ?? _translations['en']![key] ?? key;
  }

  /// Translate a key with a single numeric placeholder `{n}`.
  /// Example: tr key `'add_n_points'` = `'Add {n} more points'`
  ///          → `trN('add_n_points', 3)` returns `'Add 3 more points'`
  String trN(String key, int n) => tr(key).replaceAll('{n}', '$n');

  bool get isRtl => locale.languageCode == 'ar';
}

class AppLocalizationsDelegate
    extends LocalizationsDelegate<AppLocalizations> {
  const AppLocalizationsDelegate();

  @override
  bool isSupported(Locale locale) =>
      const ['en', 'ar'].contains(locale.languageCode);

  @override
  Future<AppLocalizations> load(Locale locale) async =>
      AppLocalizations(locale);

  @override
  bool shouldReload(AppLocalizationsDelegate old) => false;
}

/// Convenience extension so screens can do `context.tr('key')`.
extension TranslateX on BuildContext {
  String tr(String key) => AppLocalizations.of(this).tr(key);

  /// Translate with a single numeric `{n}` placeholder.
  String trN(String key, int n) => AppLocalizations.of(this).trN(key, n);

  bool get isRtl => AppLocalizations.of(this).isRtl;
}
